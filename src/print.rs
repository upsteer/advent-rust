pub fn printing(){
	println!("This is romit");

	println!("This is {} with age {}", "romit", "25");

	println!("This is {0} living in {1}", "romit", "dharan");

	println!("This is {name} with age {age}", name="romit", age="25");

	println!("Printing Array: {:?}", (0, 10, "Romit"));

	println!("Binary: {:b}, Hex: {:x}, Oct: {:o}", 11,11,11);
}